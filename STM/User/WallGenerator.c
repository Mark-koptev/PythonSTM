void wall_initialize()
{
	if(wall_initialized)
		return;
	
	wall_map = (char**) malloc((sizeof(char**)) * (CELL_HEIGHT+1));
	
	for(int i = 0; i < CELL_HEIGHT + 1;  i++)
	{
		wall_map[i] = (char*) malloc((sizeof(char*)) * CELL_WIDTH);
	}
	
	wall_initialized = 1;
}
void wall_generate_map()
{
	for(int i = 0; i < CELL_HEIGHT + 1;  i++)
	{
		for(int j = 0; j < CELL_WIDTH; j++)
		{
			wall_map[i][j] = wall_patterns[wall_map_index][i][j];
			if(wall_map[i][j])
			{
				draw_image(IMG_WALL, j, i);
			}
		}
	}
}
void wall_select_map(int index)
{
	wall_map_index = index;
}
int wall_has_collision(int x, int y)
{
	return wall_map[y][x] == 1;
}
